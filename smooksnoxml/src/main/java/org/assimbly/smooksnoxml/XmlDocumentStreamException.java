package org.assimbly.smooksnoxml;

public class XmlDocumentStreamException extends RuntimeException {

    public XmlDocumentStreamException() {
    }

    public XmlDocumentStreamException(String s) {
        super(s);
    }

    public XmlDocumentStreamException(String s, Throwable throwable) {
        super(s, throwable);
    }

    public XmlDocumentStreamException(Throwable throwable) {
        super(throwable);
    }
}
